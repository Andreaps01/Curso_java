public class JavaApplication1 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
  //variable byte
  byte b1=10;
  byte b2=0xa;
  System.out.println("variable b1:"+b1);
  System.out.println("variable b2:"+b2);
  
  //variable short
  short s1=2;
  System.out.println("varianle s1:"+s1);
  
  //variable char
  char ch1=65, ch2='A';
  System.out.println("variable ch1:"+ch1);
  System.out.println("variable ch2:"+ch2);
  
  //variables enteras
  int decimal=100;
  int octal=0144;
  int hexa=0*64;
  System.out.println("variable decimal:"+decimal);
  System.out.println("variable octal:"+octal);
  System.out.println("variable hexa:"+hexa);
  
  //variable long
  long  long1=10,long2=20L;
  System.out.println("variable long1:"+long1);
  System.out.println("variable long2:"+long2);
  //variable float
  float f1=15,f2=22.3F;
  System.out.println("variable f1:"+f1);
  System.out.println("variable f2:"+f2);
  //variable double
  double d1=11.0,d2=30.15D;
  System.out.println("variable d1:"+d1);
  System.out.println("variable d2:"+d2);
    }
    
}
